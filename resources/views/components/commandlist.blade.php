                const commandUsage = card.querySelector('.text-xs.text-base-content\\/60').textContent.toLowerCase();
                
                // Mencari di nama, deskripsi, dan usage
                const isVisible = commandName.includes(searchTerm) || 
                                commandDesc.includes(searchTerm) || 
                                commandUsage.includes(searchTerm);
                
                if (isVisible) {
                    hasVisibleCommand = true;
                    // Clone card dan tambahkan informasi kategori
                    const clonedCard = card.cloneNode(true);
                    const categoryBadge = document.createElement('div');
                    categoryBadge.className = 'text-xs text-base-content/60 mt-2';
                    categoryBadge.textContent = `Kategori: ${categoryName}`;
                    clonedCard.appendChild(categoryBadge);
                    
                    // Hitung relevansi untuk pengurutan
                    let relevance = 0;
                    if (commandName.includes(searchTerm)) relevance += 3;
                    if (commandDesc.includes(searchTerm)) relevance += 2;
                    if (commandUsage.includes(searchTerm)) relevance += 1;
                    
                    matchedCommands.push({
                        element: clonedCard,
                        relevance: relevance
                    });
                }
                
                card.style.display = isVisible ? 'block' : 'none';
            });
            
            // Buka kategori jika ada command yang cocok
            category.querySelector('.category-toggle').checked = hasVisibleCommand;
            // Sembunyikan kategori yang tidak memiliki hasil
            category.style.display = hasVisibleCommand ? 'block' : 'none';
        });

        // Urutkan hasil berdasarkan relevansi
        matchedCommands.sort((a, b) => b.relevance - a.relevance);

        // Tampilkan hasil pencarian
        searchResults.classList.remove('hidden');
        searchResultsContent.innerHTML = '';

        if (matchedCommands.length > 0) {
            // Ada hasil pencarian
            emptySearchResult.classList.add('hidden');
            matchedCommands.forEach(command => {
                searchResultsContent.appendChild(command.element);
            });
        } else {
            // Tidak ada hasil
            searchResultsContent.innerHTML = '';
            emptySearchResult.classList.remove('hidden');
        }
    });
});
